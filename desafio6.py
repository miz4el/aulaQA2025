valor = int(input("Digite um valor: "))
print(f"o valor inserido é de: {valor}")
#declarando o valor inicial

valor1 = valor*2
print(f"o dobro do valor inserido é de {valor1}")
#declarando o valor1

valor2 = valor*3
print(f"o triplo do valor inserido é de {valor2}")
#declarando o valor2

valor3 = valor**2
print(f"o valor inserido ao quadrado é igual a: {valor3}")
#declarando o valor3

valor4 = valor**(1/2)
print(f"a raiz quadrada do valor inserido é igual a: {valor4:.2f}")
#declarando o valor4

valor5 = valor**(1/3)
print(f"a raiz cubica do valor inserido é igual a: {valor5:.2f}")
#declarando o valor5
